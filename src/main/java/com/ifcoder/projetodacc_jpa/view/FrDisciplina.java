    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Disciplina disciplinaExcluida = (Disciplina) this.getObjetoSelecionadoNaGrid();

        if (disciplinaExcluida == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {

            int response = JOptionPane.showConfirmDialog(null,
                    "Deseja exlcuir o Aluno  \n("
                    + disciplinaExcluida.getNome() + ", "
                    + disciplinaExcluida.getCurso() + ") ?",
                    "Confirmar exclusÃ£o",
                    JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.OK_OPTION) {

                try {
                    this.idDisciplinaEditando = disciplinaExcluida.getId();
                    disciplinaController.excluirDisciplina(this.idDisciplinaEditando);

                    disciplinaController.atualizarTabela(grdAlunos);
                    JOptionPane.showMessageDialog(this, "ExclusÃ£o feita com sucesso!");
                } catch (ProfessorException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Disciplina disciplinaEditando = (Disciplina) this.getObjetoSelecionadoNaGrid();

        if (disciplinaEditando == null)
            JOptionPane.showMessageDialog(this, "Primeiro selecione um registro na tabela.");
        else {
            this.limparCampos();
            this.habilitarCampos(true);
            this.preencherFormulario(disciplinaEditando);
            this.idDisciplinaEditando = disciplinaEditando.getId();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    /**
     * Retorna o objeto (classe) da linha selecionada na grid.
     *
     * Uso a estratÃ©gia do metodo getValueAt() la na TableModel, receber coluna
     * -1 e retornar o objeto ao inves de uma cÃ©lula.
     *
     * @return
     */
    private Object getObjetoSelecionadoNaGrid() {
        int rowCliked = grdAlunos.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdAlunos.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }

