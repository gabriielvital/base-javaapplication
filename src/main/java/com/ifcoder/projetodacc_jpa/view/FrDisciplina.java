    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (idDisciplinaEditando > 0) {
                disciplinaController.atualizarDisciplina(idDisciplinaEditando, edtNome.getText(), edtCurso.getText(), edtSemestre.getText());
            } else {
                disciplinaController.cadastrarDisciplina(edtNome.getText(), edtCurso.getText(), edtSemestre.getText());
            }
            //Comando bastante importante
            this.idDisciplinaEditando = -1L;

            disciplinaController.atualizarTabela(grdAlunos);

            this.habilitarCampos(false);
            this.limparCampos();
        } catch (AlunoException e) {
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

